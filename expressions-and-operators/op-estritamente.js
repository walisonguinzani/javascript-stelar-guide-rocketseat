//8.7 Operatores de Comparacao Estritos

// ira comparar valores e retornar um boolean como resposta a comparacao

let one = 1
let two = 2

// === estritamente igual a
console.log (one === "1")
console.log (one === 1)

// !== estritamente diferente de
console.log (two !== 1)
console.log (two !== "1")