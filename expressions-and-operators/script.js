//8.14 Operators Precedence

/*
    * grouping                      ( )
    * negacao e incremento          ! ++ --
    * multiplicacao e divisao       * /
    * adicao e subtracao            + -
    * relacional                    < <= > >=
    * igualdade                     == != === !==
    * and                           &&
    * or                            ||
    * condicional                   ?:
    * assignment (atribuicao)       = += -= *=
*/

console.log( ( 2 + 5 ) * 10 )
console.log( true == 1 )
console.log( false == 0 )
console.log( 3 > 2 == 1 )
console.log( 3 > 2 && 2 > 1 )
