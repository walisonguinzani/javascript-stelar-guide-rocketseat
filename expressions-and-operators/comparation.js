//8.8 Operatores de Comparacao maior e menor

let one = 1
let two = 2

// >       Maior que
console.log(one > two)

// >=      Maior ou igual a que
console.log(one >= 1)
console.log(two >= 1)

// <       Menor que
console.log(one < two)

// <=      Menor ou igaul a que
console.log(one <= two)
console.log(one <= 1)
console.log(one <= 0)