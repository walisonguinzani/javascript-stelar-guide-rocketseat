// condition ? value1 : value2


// Exemples

// Top Breakfast
let pao = true
let queijo = false

const niceBreakfast = pao || queijo ? 'Top Breakfast' : 'Bad Breakfast'

console.log(niceBreakfast)

// Biggest than 18

let age = 16

const canDrive = age >= 18 ? 'Can drive' : "Can't drive"

console.log(canDrive)
